#include "../../hdr/system/shutdown.h"

//----------------------------------------------------------------------------------------------------------------------
//
// Shutdown with an error
void sys_shutdownWithError(const std::string& errorMessage)
//----------------------------------------------------------------------------------------------------------------------
{
	sys_freeMemory ();
    cout << errorMessage << endl;

    exit(-1);
}

//----------------------------------------------------------------------------------------------------------------------
//
// Shutdown normally
void sys_shutdown()
//----------------------------------------------------------------------------------------------------------------------
{
	paraScriptInstance.stop();
	sys_freeMemory ();

	logFile.close();

	exit(0);
}